my_list = list([2, 4, 6, 8, 10, 12])

# remove item present at index 2
my_list.pop(2)
print(my_list)
# Output [2, 4, 8, 10, 12]

# remove item without passing index number
my_list.pop()
print(my_list)
# Output [2, 4, 8, 10]
my_list.pop()
print(my_list)
my_list.pop()
print(my_list)
my_list.pop()
print(my_list)
my_list.pop()
print(my_list)
